    public function findVilles( SearcheCampus $search)
    {
        $query= $this->createQueryBuilder('c')
        ->addOrderBy('c.nom', 'DESC')
        ->setMaxResults(20);
        if(!empty($search->q))
        {
            $query = $query
                ->andWhere('c.nom LIKE :q')
                ->setParameter('q', $search->q);
        }

        $query= $query->getQuery();
        return $query->getResult();
    }
